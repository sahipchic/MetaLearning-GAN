    import matplotlib.pyplot as plt

    def column(matrix, i):
        return [row[i] for row in matrix]

    n = 329
    x = np.array(column(data_test, 0))[:n] * 450.0 / 329.0
    y1 = np.array(column(column(data_test, 1), 1))[:n]
    y2 = np.array(column(column(data_test, 1), 3))[:n]
    z1 = savgol_filter(y1, window_length=251, polyorder=4)
    z2 = savgol_filter(y2, window_length=251, polyorder=4)

    fig, ax = plt.subplots()
    plt.plot(x, y1, color='r', linestyle='-', linewidth=1, alpha=.1)
